# Comparison Operators
# Comparison operators compare values and return True or False.

# ==	equal to
# !=	not equal to
# >	greater than
# <	less than
# >=	greater then or equal to
# <=  less than or equal to

print(5 == 5)   # True
print(5 != 3)   # True
print(10 > 5)   # True
print(10 < 5)   # False
print(10 >= 10) # True
print(10 <= 5)  # False